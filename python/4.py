def insertion_sort(arr):
    # Проходим по всем элементам массива, начиная со второго
    for i in range(1, len(arr)):
        key = arr[i]  # Текущий элемент для вставки
        j = i - 1

        # Сдвигаем элементы массива, которые больше ключа, на одну позицию вправо
        while j >= 0 and key < arr[j]:
            arr[j + 1] = arr[j]
            j -= 1

        # Вставляем ключ на правильное место
        arr[j + 1] = key


# Ввод последовательности чисел
user_input = input("Введите последовательность чисел, разделённых пробелами: ")
numbers = list(map(int, user_input.split()))

# Сортируем и выводим результат
insertion_sort(numbers)
print("Отсортированная последовательность:", numbers)
